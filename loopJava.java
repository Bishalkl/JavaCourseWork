public class loopJava {
    public static void main(String[] args) {
         //for loop syntax and test
         for(int i = 1; i < 11; i++){
            System.out.println(i);
        }
        
        //while loop syntax and test
        int a = 1;
        while(a < 11){
            System.out.println("Hello world");
            a++;
        }
        
        // do while loop syntax and test
        int b = 1;
        do{
            System.out.println(b);
            b++;
        }while( b < 11);
    }
}
